public class GameEntity {
    private int damage;
    private int life;

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getDamage() {
        return damage;
    }

    public int getLife() {
        return life;
    }

}
